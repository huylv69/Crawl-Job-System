    def parse_src(self, response):
        self.item = CompanyItem()
        self.item["url"] = response.request.url

        address = response.xpath('//*[@id="box_chi_tiet_nha_tuyen_dung"]/div/div[2]/div[1]/p[1]/text()').extract()
        if len(address) > 0:
            self.item["address"] = address[1].strip()

        name = response.xpath('//*[@id="box_chi_tiet_nha_tuyen_dung"]/div/div[2]/h1/text()').extract()
        if len(name) > 0:
            self.item["name"] = name[0].strip()

        scale = response.xpath('//*[@id="box_chi_tiet_nha_tuyen_dung"]/div/div[2]/div[1]/p[2]/text()').extract()
        if len(scale) > 0:
            self.item["scale"] = scale[1].strip()

        website = response.xpath('//*[@id="box_chi_tiet_nha_tuyen_dung"]/div/div[2]/div[1]/p[3]/span/text()').extract()
        if len(website) > 0:
            self.item["website"] = website[0].strip()

        about = response.xpath('//*[@id="box_chi_tiet_nha_tuyen_dung"]/div/div[2]/div[2]/text()').extract()
        if len(about) > 0:
            des = ' '.join(about).strip()
            self.item["about"] = "\n".join(des.splitlines())

        logo = response.xpath('//*[@id="box_chi_tiet_nha_tuyen_dung"]/div/div[1]/div[1]/img/@src').extract()
        if len(logo) > 0:
            self.item["logo"] = logo[0]

        if self.item["name"] != "":
            yield self.item
